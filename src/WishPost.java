public class WishPost {
    private int wishPostID;
    private int postID;
    private int UserID;

    public int getWishPostID() {
        return wishPostID;
    }

    public void setWishPostID(int wishPostID) {
        this.wishPostID = wishPostID;
    }

    public int getPostID() {
        return postID;
    }

    public void setPostID(int postID) {
        this.postID = postID;
    }

    public int getUserID() {
        return UserID;
    }

    public void setUserID(int userID) {
        UserID = userID;
    }
}
